package au.com.grieve.portalnetwork.utils;

import org.bukkit.block.BlockFace;

public class Utils {

  private static final BlockFace[] axis = {
    BlockFace.NORTH, BlockFace.EAST, BlockFace.SOUTH, BlockFace.WEST
  };
  private static final BlockFace[] radial = {
    BlockFace.NORTH,
    BlockFace.NORTH_EAST,
    BlockFace.EAST,
    BlockFace.SOUTH_EAST,
    BlockFace.SOUTH,
    BlockFace.SOUTH_WEST,
    BlockFace.WEST,
    BlockFace.NORTH_WEST
  };

  public static BlockFace yawToFace(float yaw, boolean useSubCardinalDirections) {
    /*
    Found at: https://www.spigotmc.org/threads/player-get-facing-direction.173083/
     */

    if (useSubCardinalDirections) return radial[Math.round(yaw / 45f) & 0x7].getOppositeFace();

    return axis[Math.round(yaw / 90f) & 0x3].getOppositeFace();
  }
}
